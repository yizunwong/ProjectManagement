    private void submitBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_submitBtnActionPerformed
        // TODO add your handling code here:
        UserController.Account account = userController.new Account();

        String email = emailField.getText().trim();
        String pass = passwordField.getText().trim();
        List<String> errors = new ArrayList<>();
        validateEmail(email, errors);

        if (errors.isEmpty()) {
            if (account.checkConfidential(email, pass)) {
                if (account.getRole().equalsIgnoreCase("student")) {
                    StudentMain studentMain = new StudentMain();
                    studentMain.setUser(account.getId());
                    studentMain.setHeader("Student Dashboard");

                    dispose();
                    studentMain.setVisible(true);
                } else if (account.getRole().equalsIgnoreCase("admin")) {
                    AdminMenu adminMenu = new AdminMenu();
                    UserController.Admin admin = userController.new Admin();

                    adminMenu.setHeader(admin.getName(), "Admin Dashboard");
                    dispose();
                    adminMenu.setVisible(true);
                } else if (account.getRole().equalsIgnoreCase("lecturer")) {
                    LecturerMain main = new LecturerMain();
                    dispose();
                    main.setUser(account.getId());
                    main.setHeader("Lecturer Dashboard");

                    main.setVisible(true);
                } else if (account.getRole().equalsIgnoreCase("project manager")) {
                    ProjectManagerMenu main = new ProjectManagerMenu();
                    dispose();
                    main.setUser(account.getId());
                    main.setHeader("Project Manager Dashboard");
                    main.setVisible(true);
                }
            } else {
                JOptionPane.showMessageDialog(null, "Password is wrong", "Password Not Match", JOptionPane.WARNING_MESSAGE);
            }
        } else {
            JOptionPane.showMessageDialog(null, errors.get(0), "Validation Error", JOptionPane.WARNING_MESSAGE);

        }

    }//GEN-LAST:event_submitBtnActionPerformed

